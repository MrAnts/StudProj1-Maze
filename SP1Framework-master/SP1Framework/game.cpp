void AIMovementLR(COORD curPlaceX)
{
	int Direction = -1;
	COORD* NextX = 0;

	NextX = &curPlaceX;
	NextX += Direction;

	if (UDLRAI1.Coord.X < 0 || UDLRAI1.Coord.X > 80)
	{
		Direction *= -1;
	}
}
void AIMovementUD(COORD curPlaceY)
{
	int Direction = -1;
	COORD* NextY = 0;

	NextY = &curPlaceY;
	NextY += Direction;

	if (UDLRAI1.Coord.Y < 1 || UDLRAI1.Coord.Y == 30)
	{
		Direction *= -1;
	}
}
void AIMovementUDLR(COORD curPlaceX, COORD curPlaceY)
{
	int Direction = -1;
	COORD* NextX = 0;
	COORD* NextY = 0;
	NextX = &curPlaceX;
	NextY = &curPlaceY;

	NextX += Direction;
	if (NextX < 0)
	{
		Direction *= -1;
	}
}
