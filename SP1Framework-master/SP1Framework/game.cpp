void update(double dt)
{

	CONSOLE_SCREEN_BUFFER_INFO csbi;
	GetConsoleScreenBufferInfo( GetStdHandle( STD_OUTPUT_HANDLE ), &csbi );

    // get the delta time
    elapsedTime += dt;
    deltaTime = dt;

//HengSoon Code Start
	AIMovementLR(LRAI1.Coord.X);
	AIMovementUD(UDAI1.Coord.Y);

	//LRAI1.Coord.X+= direction;
	//UDAI1.Coord.Y += direction2;

	if(goingRight)
	{
		direction3 = 1;
		UDLRAI1.Coord.X += direction3;
		if (UDLRAI1.Coord.X == csbi.srWindow.Right)
		{
			UDLRAI1.Coord.X = csbi.srWindow.Right;
			goingRight = false;
			goingDown = true;
		}
	}

	if(goingLeft)
	{
		direction3 = -1;
		UDLRAI1.Coord.X += direction3;
		if (UDLRAI1.Coord.X == csbi.srWindow.Left)
		{
			UDLRAI1.Coord.X = csbi.srWindow.Left;
			goingLeft = false;
			goingUp = true;
		}
	}

	if(goingUp)
	{
		direction3 = -1;
		UDLRAI1.Coord.Y += direction3;
		if (UDLRAI1.Coord.Y < 1)
		{
			goingUp = false;
			goingRight = true;
		}
	}

	if(goingDown)
	{
		direction3 = 1;
		UDLRAI1.Coord.Y += direction3;
		if (UDLRAI1.Coord.Y > 10)
		{
			UDLRAI1.Coord.Y += direction3;
			goingDown = false;
			goingLeft = true;
		}
	}

	if (UDLRAI1.Coord.X < 0 || UDLRAI1.Coord.X > csbi.srWindow.Right)
	{
		direction *= -1;
	}

	if (UDLRAI1.Coord.Y < 1 || UDLRAI1.Coord.Y == csbi.srWindow.Bottom)
	{
		direction2 *= -1;
	}
	
//HengSoon Code End
	
    // Updating the location of the character based on the key press
    if (keyPressed[K_UP] && charLocation.Y > 0)
    {
        Beep(1440, 30);
        charLocation.Y--; 
    }
    if (keyPressed[K_LEFT] && charLocation.X > 0)
    {
        Beep(1440, 30);
        charLocation.X--; 
    }
    if (keyPressed[K_DOWN] && charLocation.Y < consoleSize.Y - 1)
    {
        Beep(1440, 30);
        charLocation.Y++; 
    }
    if (keyPressed[K_RIGHT] && charLocation.X < consoleSize.X - 1)
    {
        Beep(1440, 30);
        charLocation.X++; 
    }

    // quits the game if player hits the escape key
    if (keyPressed[K_ESCAPE])
        g_quitGame = true;    
}

